package com.kh.c_operator;

public class C_산술연산자 {

	public static void main(String[] args) {
		
		//산술연산자 : + , - , *, / ,%
		int x = 10;
		int y = 3;
		double z = 10;
		
		System.out.println(x + y);
		System.out.println(x - y);
		System.out.println(x * y);
		System.out.println(x / y);
		System.out.println(z / y);
		System.out.println(x % y);
	}
	
	
	
	
	
	
	
	
	
	
}
